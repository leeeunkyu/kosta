package com.kosta.calc.control;

public class Calculator {
	public int plus(int su1,int su2) {
		return su1+su2;
	}
	public int minus(int su1,int su2) {
		return su1-su2;
	}
	public int multi(int su1,int su2) {
		return su1*su2;
	}
	public int div(int su1,int su2) {
		return su1/su2;
	}
}
